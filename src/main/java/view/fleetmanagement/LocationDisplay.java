    String lastLocation = "";
    public LocationDisplay(String lastLocation) {
        initComponents();
        this.lastLocation = lastLocation;
        
        Engine engine = Engine.newInstance(
                EngineOptions.newBuilder(HARDWARE_ACCELERATED).licenseKey("1BNDHFSC1G8EU0JPOCG014Z1AW8OJJ2IIMCSCYOXQMFTPV68JZVMEV1X8QWDQ6U8OMMQ21").build());
        Browser browser = engine.newBrowser();

        SwingUtilities.invokeLater(() -> {
            // Creating Swing component for rendering web content
            // loaded in the given Browser instance.
            BrowserView view = BrowserView.newInstance(browser);
            
            JFrame frame = new JFrame();
            
            // Close Engine and onClose app window
            frame.addWindowListener(new WindowAdapter() {
                @Override
                public void windowClosing(WindowEvent e) {
                    engine.close();
                    dispose();
                }
            });
            frame.setDefaultCloseOperation(WindowConstants.DISPOSE_ON_CLOSE);
            JTextField addressBar = new JTextField("https://www.google.com/maps/search/?api=1&query="+this.lastLocation+"Airport");
            addressBar.addActionListener(e ->
                    browser.navigation().loadUrl(addressBar.getText()));
            frame.add(addressBar, BorderLayout.NORTH);
            frame.add(view, BorderLayout.CENTER);
            frame.setSize(800, 950);
            frame.setLocationRelativeTo(null);
            frame.setVisible(true);
            setSize(0, 0);
            browser.navigation().loadUrl(addressBar.getText());
        });
    }
        
        
    

