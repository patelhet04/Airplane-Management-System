    private void searchFlightFieldKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchFlightFieldKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode() == 10){
        
            String planeNumber = searchFlightField.getText();
            
            String[] columnNames = {"Flight", "Model", "Seats", "Engine", "aisle"};
            DefaultTableModel model = new DefaultTableModel(columnNames, 0){
                @Override
                public boolean isCellEditable(int row, int column) {
                   //all cells false
                   return false;
                };
            };
            
            Document doc =  crud.getFirstRecordByKey("flight", planeNumber, this.airplane) ;
            
            if(doc == null){
                JOptionPane.showMessageDialog(this,
                 "No Data Found!",
            "Plane Data",
            JOptionPane.ERROR_MESSAGE);
            }
            model.addRow(new Object[] { doc.get("flight").toString(), doc.get("model"), doc.get("seats"), doc.get("engine"), doc.get("aisle") });
            flightsTable.setModel(model);
        }
    }//GEN-LAST:event_searchFlightFieldKeyPressed

    private void flightsTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_flightsTableMouseClicked
        // TODO add your handling code here:
        JTable source = (JTable)evt.getSource();
        int row = source.getSelectedRow();
        
        
        String selectedFlight = (String) source.getValueAt(row, 0);
        ObjectId id = (ObjectId) crud.getFirstRecordByKey("flight", selectedFlight, this.airplane).get("_id");
        
        FleetManagement fm = new FleetManagement(database, id);
        fm.setDefaultCloseOperation(1);
        
    }//GEN-LAST:event_flightsTableMouseClicked

