    public void populateTable(String org){
    
        MongoCollection<Document> airplane = crud.getCollection("airplanes", this.database);
        
        FindIterable<Document> plane = airplane.find(new Document("company", org));
        
        
        MongoCursor<Document> cursor = plane.iterator();
        String[] columnNames = {"Flight", "Model", "Seats", "Engine", "aisle"};
        DefaultTableModel model = new DefaultTableModel(columnNames, 0){
                @Override
                public boolean isCellEditable(int row, int column) {
                   //all cells false
                   return false;
                };
        };
        try {
            while(cursor.hasNext()) {               
                Document doc = cursor.next();
                
                model.addRow(new Object[] { doc.get("flight").toString(), doc.get("model"), doc.get("seats"), doc.get("engine"), doc.get("aisle") });
            }
        } finally {
            cursor.close();
        }
        
        flightsTable.setModel(model);
    
    }

