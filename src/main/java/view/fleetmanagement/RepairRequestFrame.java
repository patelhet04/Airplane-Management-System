    private void submitRequestBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_submitRequestBtnMouseClicked
                   
        try {
            SimpleDateFormat sdf= new SimpleDateFormat("MM/dd/yyyy");
            Date dueDate=sdf.parse(dueDateTF.getText());
            String repairPart= repairPartTF.getText();
            String description= descriptionTF.getText();
            if(repairPart.isBlank() || description.isBlank() || !dueDate.after(new Date())){
                // invalid data
                JOptionPane.showMessageDialog(this,
            "Repair part and Description Part is required and Due date should not be past dates",
            "Field Validations",
            JOptionPane.ERROR_MESSAGE);    
                return;
            }
            Users user= ActiveUser.getActiveUser();
            //store the data
            Repair repairRequest= new Repair(null, (ObjectId)this.id,new Date(), "Requested", dueDate, repairPart, user.getId(), description);
            boolean inserted=this.repairService.insertRepairRequest(repairRequest);
            if(!inserted){
                   JOptionPane.showMessageDialog(this,"Request generation fail",
            "Field Validations",
            JOptionPane.ERROR_MESSAGE);
                   
                   return;
            }else{
                JOptionPane.showMessageDialog(this,"Request generated successfully",
            "Field Validations",
            JOptionPane.INFORMATION_MESSAGE);
            dueDateTF.setText("");
            repairPartTF.setText("");
            descriptionTF.setText("");
            this.dispose();
            }
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this,"Ensure date is in mm/dd/yyyy format",
            "Field Validations",
            JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(RepairRequestFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_submitRequestBtnMouseClicked


