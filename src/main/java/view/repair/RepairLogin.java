    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String userName = usernameTxt.getText();
        String passWord = passTxt.getText();

        Document user = new Login().getUser(userName, "users", "Repair Manager");
        System.out.println(user);
        if(user != null){
           
            Boolean userFound = new Login().comparePassword(user.get("password").toString(), passWord);
            System.out.println(userFound+" userFound");
        
            if(userFound){

                RepairHome home = null;
                try {
                    home = new RepairHome(user.get("name").toString(), (ObjectId) user.get("_id"));
                } catch (ParseException ex) {
                    Logger.getLogger(RepairLogin.class.getName()).log(Level.SEVERE, null, ex);
                }
                home.setDefaultCloseOperation(1);
            }
            else{

                JOptionPane.showMessageDialog(this,"Incorrect Username or Password");

                usernameTxt.setText("");
                passTxt.setText("");
            }
        }else{
            JOptionPane.showMessageDialog(this,"User does not exist");
            usernameTxt.setText("");
            passTxt.setText("");
        }
        
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed



